set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

union_set = set1 | set2
print("Union:", union_set)

intersection_set = set1 & set2
print("Intersction:", intersection_set)

difference_set = set1 - set2
print("Differnce:", difference_set)

sym_diff_set = set1 ^ set2
print("Symmetric Differnce:", sym_diff_set)